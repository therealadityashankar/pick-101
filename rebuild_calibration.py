def main():
    rows = []
    for line in RAW.splitlines():
        line = line.strip()
        m = re.search(r'DATA:\s*\(([^)]+)\)', line)
        if not m:
            continue
        vals = [float(v) for v in m.group(1).split(',')]
        if len(vals) != 5:
            print(f"Skipping malformed line: {line}")
            continue
        rows.append(vals)

    if not rows:
        print("No DATA lines found — paste them between the triple-quotes in RAW.")
        return

    arr = np.array(rows, dtype=np.float32)
    np.savez(
        "calibration_jenga.npz",
        data           = arr,
        true_x         = arr[:, 0],
        true_y         = arr[:, 1],
        obs_x          = arr[:, 2],
        obs_y          = arr[:, 3],
        heights        = arr[:, 4],
        inset_mm       = np.float32(INSET_MM),
        jenga_block_mm = np.float32(15.0),
    )
    print(f"Saved calibration_jenga.npz  ({len(arr)} measurements)")
    print(f"Heights present: {sorted(set(arr[:,4].tolist()))}")


if __name__ == "__main__":
    main()
